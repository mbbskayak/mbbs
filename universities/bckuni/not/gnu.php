<h3>VISION</h3>
<p>Be a prestigious and value-based university serving society through high-quality education and research, putting personal development at the core.</p>


<H3>HISTORY </H3>
<p>Georgian National University SEU is one of the most distinguished universities in the higher education space of our country with a history of 20 years</p>

<p>SEU has been focusing on development since its establishment. The University aims to create a university environment in line with international standards, where the synthesis and internationalization of strong theoretical, practical and research components in the learning process will ensure the training of competitive professionals for national and international markets.</p>

<p>In a short period of time, the University has been able to gain trust and popularity in the Georgian educational space - with educational programs meeting modern standards and requirements, with the efforts of highly qualified academic staff, effective teaching methods, introduction of new technologies and innovative approaches, which makes SEU very attractive for students and its graduates' - for employers!</p>
<p>In 2009, SEU was accredited by the National Center for Education Accreditation, and in 2014 underwent re-authorization - which confirms the full compliance of the University with the goals of higher education in Georgia and the European Higher Education Area.</p>
