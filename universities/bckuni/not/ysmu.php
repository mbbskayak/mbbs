<h3>HISTORY</h3>
<p>Prior to the establishment of the First Republic of Armenia, young Armenians were educated in Russian and European universities</p>

<p>As in any country, the leadership of the First Republic of Armenia, along with many serious issues, pays priority attention to the development of the country's caring life, in particular, education.

The opening of a university in Armenia is becoming an issue especially after the closure of a private university in Georgia.

1919 On May 16, the Council of Ministers of the Republic of Armenia decided to establish a university in Yerevan with 4 departments: historical-linguistic, economic-legal, medical, physical-mathematical-technical subdivisions.

It is planned to open the historical-linguistic department first of all in the 1919-1920 seminary year.</p>
<h3>Results</h3>
<p>1920 On January 31, the university temporarily reopened in the building of the Alexandropol Business School, as there were no proper building conditions in Yerevan. The opening ceremony was attended by the Prime Minister of the Republic A. Khatisyan, Minister of Culture and Public Education N. Aghbalyan, many guests from Armenia and other countries. From the new academic year the university moved to the building of the Yerevan Teachers' Seminary, called the People's University of Armenia, and from 1922 to the State University.</p>
