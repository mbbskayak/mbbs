<h3>History of University</h3>

<p>The idea for the university first emerged in the early 19th century as the struggle for liberation from the Russian empire intensified. A new generation was, led by the great Georgian public figure Ilia Chavchavadze, who initiated a new stage of spiritual revival in the history of our country beginning in the 1860s. Real possibilities for establishing an institution of higher learning appeared when the Nobiliary Manorial Estate Bank was founded in Tbilisi. It was the only bank in the Russian empire, that contributed to promote the cultural, educational and economic interests of the nation.</p>
<p><strong>1.</strong> First Year   - 8000 USD <br>
<strong>2.</strong> Second Year   - 8000 USD <br>
<strong>3.</strong> Third Year  - 8000 USD <br>
<strong>4.</strong> Fourth Year  - 8000 USD <br>
<strong>5.</strong> Fifth Year   - 8000 USD <br>
<strong>6.</strong> Sixth Year   - 8000 USD <br>
</p>

<p>Thus, the first university in the Caucasus opened its doors on January 26, 1918, the day of the commemoration of King David the Builder (later this commemoration was established on February 8). Nominated by Ivane Javakhishvili, Petre Melikishvili was elected by the Council of Professors as first Rector of the University. Ivane Javakhishvili took charge of the Faculty of Philosophy.</p>

<p>The following persons became Lecturers at the newly established university: Ivane Javakhishvili, Iustine Abuladze, Giorgi Akhvlediani, Philipe Gogichaishvili, Ekvtime Takaishvili, Korneli Kekelidze, Shalva Nutsubidze, Elisabed Bagrationi-Orbeliani, Dimitri Uznadze, Vanda Gambashidze, Ilia Kipshidze, Ioseb Kipshidze, Akaki Shanidze, Andria Benashvili, Andria Razmadze, Ivane Beritashvili, and Grigol Tsereteli.</p>

