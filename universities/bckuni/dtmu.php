<h3>HISTORY</h3>
<p>“AIETI” Medical School is a branch of David Tvildiani Medical University. Its mission is to provide scientifically-oriented Higher Medical Education based on the best International experience.</p>
<p><strong>1.</strong> First Year   - 8000 USD <br>
<strong>2.</strong> Second Year   - 8000 USD <br>
<strong>3.</strong> Third Year  - 8000 USD <br>
<strong>4.</strong> Fourth Year  - 8000 USD <br>
<strong>5.</strong> Fifth Year   - 8000 USD <br>
<strong>6.</strong> Sixth Year   - 8000 USD <br>
</p>
<p>After developing Residency, PHD and Doctorate programs and adding the Nursing School, AIETI Medical School has been upgraded by the National Authorization Council to the status of an University and from 2011 is named after its founder and the first rector as “David Tvildiani Medical University”.</p>

<p>AIETI Medical School Has been recognized by the WHO (World Health Organization) and listed in “ World Directory of Medical Schools” in 1995. Shortly after that, it’s diploma hasbeen verified and authorized by the Medical Examination Committees of the USA, different countries of Europe and Asia.</p>

<p>Academician David Tvildiani - outstanding Georgian cardiologist, great educator, consummate master of medical art - was a bright representative of Georgian Medical Society, distinguishably combining in himself personal greatness, perfect professionalism in medical field and incomparable teaching talent, creating his era in Georgian Medicine. David Tvildiani initiated and founded a new direction in Medicine – Heliomagnetic Cardiology. He was first and permanent Rector of “AIETI” Medical School until the last days of his life.</p>

