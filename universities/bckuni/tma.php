<h3>HISTORY</h3>
<p>Tbilisi Medical Academy was founded in 1992 by notable Georgian physician, pediatric surgeon, Petre Shotadze. After graduation of Tbilisi State Medical University in 1970, whole his doctoral, scientific and academic activities were connected to Tbilisi State Institute of Postgraduate Education for Physicians, where he went from being a postgraduate student to the Head of Pediatric Surgery and to the Rector. In 1999, following the demise of Dr. Shotadze, Tbilisi Medical Academy was renamed as “Petre Shotadze Tbilisi Medical Academy”. Since its foundation, TMA has grown up more then 1700 graduates who work successfully in leading Georgian and International Hospitals, Universities and Scientific Centres.</p>
