<h3>MISSION</h3>
<p>TMA, as a student-oriented higher education institution, primarily seeks to teach and develop national and international healthcare professionals in an innovative manner by using evidence-based knowledge, whilst practically benefiting the community in Georgia through research-based outreach activities.</p>

<p>The main Goal of Academy is to elaborate independent and critical thinking, scientific reasoning abilities, and reasonable judgments among students, in order to support their development into highly qualified specialists.</p>
<h3>VISION</h3>
<p>Tbilisi Medical Academy (TMA) aims to create an attractive environment for studying, scientific research and continuous development for students, researchers and academic personal.
In response to the always-changing medical field and modern challenges, TMA thrives to establish its position both on national and global educational map through promotion of academic excellence and critical thinking.
The Academy acknowledges its responsibilities towards the public and strives to cope with existing challenges and justify high expectations.</p>
