<h3>Mission</h3>
<p>Ilia State University mission is based on its values and vision. It clearly defines the university’s role in encouraging societal development, as well as its functions to be performed within the next seven years.

The ISU mission is to generate, disseminate and apply knowledge to advance science and contribute to the society both at local and international levels.
 
The ISU mission relies on the three following principles of unity:
Unity of research and teaching; knowledge is generated and disseminated by the same individuals, while students are provided with an opportunity to engage in the creation of knowledge;
Unity of general and specialized education: ISU is one of the first universities to have introduced significant elements of liberal arts into Georgia’s higher educational programmes, combining them with strict requirements for specialized education;
Unity of the universal and local, which combines generating universally applicable scientific knowledge with regional, national and local focuses, and a clear understanding of the civic mission of the university.</p>
