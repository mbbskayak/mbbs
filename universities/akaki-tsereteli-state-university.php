<h3>HISTORY</h3>
<p>The history of Akaki Tsereteli State University started eight decades ago and now, it is a higher educational institution in Georgia distinguished with its excellent traditions, which have found a decent place in the cultural-intellectual and moral upbringing of generations. The University has gained universal recognition as a result of staff training, high quality scientific potential and wide- ranging links with scientific institutes of advanced countries.</p>
