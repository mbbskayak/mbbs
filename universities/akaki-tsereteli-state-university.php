<p>Kutaisi Akaki Tsereteli State University, in fact, was founded in the entrails of Ivane Javakhishvili Tbilisi State University. In July 1930, the authorities abolished Tbilisi State University and created four independent higher education institutions at its base. One of them was the State Pedagogical Institute, which, from then on, until 1933, was functioning in Tbilisi. In 1933, the institute continued functioning in Kutaisi from February 13 of the same year. In 1933-1990 the institution was in the position of Alexander Tsulukidze State Pedagogical Institute.</p>

<h3>MISSION</h3>
<p>Akaki Tsereteli State University is a multilevel higher education institution that performs academic and professional programs and is engaged in active research activities.The University, based on the traditions of the Pazisi and Gelati Academies, the principles of "Great Charter of Universities"and the national and European standards for higher education, strives to provide high quality education and research activities, to promote students' personal development, to train professionals and leaders. The University is aware of its responsibility toward the society and is aimed at contributing to the development of the city, region and country as a whole. For this purpose, it provides the provision of life-long continuing education programs and services, generation and distribution of new knowledge and together with various stakeholders is involved in the formation of public policy</p>

